public void datatable () {
    Object [] baris = {"Kode Barang","Kode Jenis Barang","Nama Barang"};
        tabmode =  new DefaultTableModel(null, baris);
        table.setModel(tabmode);
        String sql = "select * from jenisbarang";
         try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("Kode_Barang");
                String b = hasil.getString("Kode");
                String c = hasil.getString("Nama_Barang");
                
                String [] data={a,b,c};
                tabmode.addRow(data);
            }
        } catch (Exception e){          
        }
}

private void deleteFromDatabase(String jenisbarang) {
    String sqljenisbarang = "DELETE FROM jenisbarang WHERE Nama_Barang = ?";
    
    try {
        PreparedStatement psBarang = conn.prepareStatement(sqljenisbarang);
        psBarang.setString(1, jenisbarang);
        psBarang.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus.");
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menghapus data: " + e.getMessage());
    }
 }

